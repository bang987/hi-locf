CREATE OR REPLACE PROCEDURE PRC_PRV_INSERT_TARGET_CONTRACTS (
    p_batch_execution_id IN NUMBER,
    p_base_date          IN DATE
) IS
BEGIN
    INSERT INTO PRV_TARGET_CONTRACT (
        TARGET_ID, BATCH_EXECUTION_ID, BASE_DATE, CONTRACT_ID, CONTRACT_NO, CUSTOMER_ID, CUSTOMER_NAME, PRODUCT_CODE,
        OUTSTANDING_PRINCIPAL_AMT, CARRYING_AMOUNT, DELINQUENCY_DAYS, DEFAULT_YN, RATING_GRADE, ONE_YEAR_PD_RATE,
        COLLATERAL_VALUE_AMT, RECOVERY_RATE
    )
    SELECT SQ_PRV_TARGET_CONTRACT.NEXTVAL,
           p_batch_execution_id,
           p_base_date,
           c.CONTRACT_ID,
           c.CONTRACT_NO,
           c.CUSTOMER_ID,
           cm.CUSTOMER_NAME,
           c.PRODUCT_CODE,
           b.OUTSTANDING_PRINCIPAL_AMT,
           NVL((
               SELECT d.CLOSING_CARRYING_AMT
                 FROM LOCF_AMORTIZATION_DTL d
                WHERE d.CONTRACT_ID = c.CONTRACT_ID
                  AND d.PAYMENT_DATE <= p_base_date
                ORDER BY d.PAYMENT_DATE DESC, d.INSTALLMENT_NO DESC
                FETCH FIRST 1 ROWS ONLY
           ), NVL((
               SELECT e.INITIAL_CARRYING_AMOUNT
                 FROM LOCF_EIR_RESULT e
                WHERE e.CONTRACT_ID = c.CONTRACT_ID
                ORDER BY e.EIR_RESULT_ID DESC
                FETCH FIRST 1 ROWS ONLY
           ), b.OUTSTANDING_PRINCIPAL_AMT)),
           NVL(d.DELINQUENCY_DAYS, 0),
           NVL(d.DEFAULT_YN, 'N'),
           NVL(rg.RATING_GRADE, 'B'),
           NVL(rg.ONE_YEAR_PD_RATE, 0.0300),
           NVL(cl.COLLATERAL_VALUE_AMT, 0),
           NVL(cl.RECOVERY_RATE, 0.20)
      FROM LOAN_CONTRACT c
      JOIN CUSTOMER_MASTER cm
        ON cm.CUSTOMER_ID = c.CUSTOMER_ID
      JOIN LOAN_BALANCE b
        ON b.CONTRACT_ID = c.CONTRACT_ID
       AND b.BASE_DATE = p_base_date
      LEFT JOIN LOAN_DELINQUENCY d
        ON d.CONTRACT_ID = c.CONTRACT_ID
       AND d.BASE_DATE = p_base_date
      LEFT JOIN CUSTOMER_RATING rg
        ON rg.CUSTOMER_ID = c.CUSTOMER_ID
       AND rg.BASE_DATE = p_base_date
      LEFT JOIN LOAN_COLLATERAL cl
        ON cl.CONTRACT_ID = c.CONTRACT_ID
       AND cl.BASE_DATE = p_base_date
     WHERE c.STATUS_CODE = 'ACTIVE';
END;
