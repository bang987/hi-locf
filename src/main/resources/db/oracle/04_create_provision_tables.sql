CREATE OR REPLACE PROCEDURE PRC_PRV_CALCULATE_STAGE (p_batch_execution_id IN NUMBER) IS
BEGIN
    INSERT INTO PRV_STAGE_RESULT (STAGE_RESULT_ID, BATCH_EXECUTION_ID, TARGET_ID, CONTRACT_ID, CONTRACT_NO, STAGE_CODE, STAGE_REASON)
    SELECT SQ_PRV_STAGE_RESULT.NEXTVAL,
           BATCH_EXECUTION_ID,
           TARGET_ID,
           CONTRACT_ID,
           CONTRACT_NO,
           CASE
               WHEN DEFAULT_YN = 'Y' OR DELINQUENCY_DAYS >= 90 THEN 'STAGE3'
               WHEN DELINQUENCY_DAYS >= 30 THEN 'STAGE2'
               ELSE 'STAGE1'
           END,
           CASE
               WHEN DEFAULT_YN = 'Y' THEN '부도차주'
               WHEN DELINQUENCY_DAYS >= 90 THEN '90일이상연체'
               WHEN DELINQUENCY_DAYS >= 30 THEN '30일이상연체'
               ELSE '정상계약'
           END
      FROM PRV_TARGET_CONTRACT
     WHERE BATCH_EXECUTION_ID = p_batch_execution_id;
END;
