CREATE OR REPLACE PROCEDURE PRC_PRV_CALCULATE_PD (p_batch_execution_id IN NUMBER) IS
BEGIN
    INSERT INTO PRV_PD_RESULT (PD_RESULT_ID, BATCH_EXECUTION_ID, TARGET_ID, CONTRACT_ID, CONTRACT_NO, STAGE_CODE, ONE_YEAR_PD_RATE, LIFETIME_PD_RATE)
    SELECT SQ_PRV_PD_RESULT.NEXTVAL,
           t.BATCH_EXECUTION_ID,
           t.TARGET_ID,
           t.CONTRACT_ID,
           t.CONTRACT_NO,
           s.STAGE_CODE,
           t.ONE_YEAR_PD_RATE,
           CASE
               WHEN s.STAGE_CODE = 'STAGE1' THEN t.ONE_YEAR_PD_RATE
               WHEN s.STAGE_CODE = 'STAGE2' THEN LEAST(t.ONE_YEAR_PD_RATE * 2.5, 1)
               ELSE LEAST(t.ONE_YEAR_PD_RATE * 4, 1)
           END
      FROM PRV_TARGET_CONTRACT t
      JOIN PRV_STAGE_RESULT s
        ON s.TARGET_ID = t.TARGET_ID
     WHERE t.BATCH_EXECUTION_ID = p_batch_execution_id;
END;
/

CREATE OR REPLACE PROCEDURE PRC_PRV_CALCULATE_LGD (p_batch_execution_id IN NUMBER) IS
BEGIN
    INSERT INTO PRV_LGD_RESULT (LGD_RESULT_ID, BATCH_EXECUTION_ID, TARGET_ID, CONTRACT_ID, CONTRACT_NO, RECOVERY_RATE, LGD_RATE)
    SELECT SQ_PRV_LGD_RESULT.NEXTVAL,
           BATCH_EXECUTION_ID,
           TARGET_ID,
           CONTRACT_ID,
           CONTRACT_NO,
           RECOVERY_RATE,
           CASE WHEN (1 - RECOVERY_RATE) < 0.15 THEN 0.15 ELSE (1 - RECOVERY_RATE) END
      FROM PRV_TARGET_CONTRACT
     WHERE BATCH_EXECUTION_ID = p_batch_execution_id;
END;
/

CREATE OR REPLACE PROCEDURE PRC_PRV_CALCULATE_ECL (p_batch_execution_id IN NUMBER) IS
BEGIN
    INSERT INTO PRV_ECL_RESULT_DTL (
        RESULT_DTL_ID, BATCH_EXECUTION_ID, BASE_DATE, CONTRACT_ID, CONTRACT_NO, CUSTOMER_NAME, PRODUCT_CODE,
        STAGE_CODE, STAGE_REASON, CARRYING_AMOUNT, EAD_AMOUNT, ONE_YEAR_PD_RATE, LIFETIME_PD_RATE,
        LGD_RATE, RECOVERY_RATE, ECL_AMOUNT
    )
    SELECT SQ_PRV_ECL_RESULT_DTL.NEXTVAL,
           t.BATCH_EXECUTION_ID,
           t.BASE_DATE,
           t.CONTRACT_ID,
           t.CONTRACT_NO,
           t.CUSTOMER_NAME,
           t.PRODUCT_CODE,
           s.STAGE_CODE,
           s.STAGE_REASON,
           e.CARRYING_AMOUNT,
           e.EAD_AMOUNT,
           p.ONE_YEAR_PD_RATE,
           p.LIFETIME_PD_RATE,
           l.LGD_RATE,
           l.RECOVERY_RATE,
           ROUND(
               e.EAD_AMOUNT
               * CASE WHEN s.STAGE_CODE = 'STAGE1' THEN p.ONE_YEAR_PD_RATE ELSE p.LIFETIME_PD_RATE END
               * l.LGD_RATE,
               2
           )
      FROM PRV_TARGET_CONTRACT t
      JOIN PRV_STAGE_RESULT s ON s.TARGET_ID = t.TARGET_ID
      JOIN PRV_EAD_RESULT e ON e.TARGET_ID = t.TARGET_ID
      JOIN PRV_PD_RESULT p ON p.TARGET_ID = t.TARGET_ID
      JOIN PRV_LGD_RESULT l ON l.TARGET_ID = t.TARGET_ID
     WHERE t.BATCH_EXECUTION_ID = p_batch_execution_id;
END;
/

CREATE OR REPLACE PROCEDURE PRC_PRV_INSERT_RESULT_SUMMARY (p_batch_execution_id IN NUMBER) IS
BEGIN
    INSERT INTO PRV_ECL_RESULT_SUMMARY (
        SUMMARY_ID, BATCH_EXECUTION_ID, BASE_DATE, PRODUCT_CODE, STAGE_CODE,
        CONTRACT_COUNT, TOTAL_CARRYING_AMOUNT, TOTAL_EAD_AMOUNT, TOTAL_ECL_AMOUNT, CREATED_AT
    )
    SELECT SQ_PRV_ECL_RESULT_SUMMARY.NEXTVAL,
           summary_rows.BATCH_EXECUTION_ID,
           summary_rows.BASE_DATE,
           summary_rows.PRODUCT_CODE,
           summary_rows.STAGE_CODE,
           summary_rows.CONTRACT_COUNT,
           summary_rows.TOTAL_CARRYING_AMOUNT,
           summary_rows.TOTAL_EAD_AMOUNT,
           summary_rows.TOTAL_ECL_AMOUNT,
           CURRENT_TIMESTAMP
      FROM (
            SELECT d.BATCH_EXECUTION_ID,
                   d.BASE_DATE,
                   d.PRODUCT_CODE,
                   d.STAGE_CODE,
                   COUNT(*) AS CONTRACT_COUNT,
                   SUM(d.CARRYING_AMOUNT) AS TOTAL_CARRYING_AMOUNT,
                   SUM(d.EAD_AMOUNT) AS TOTAL_EAD_AMOUNT,
                   SUM(d.ECL_AMOUNT) AS TOTAL_ECL_AMOUNT
              FROM PRV_ECL_RESULT_DTL d
             WHERE d.BATCH_EXECUTION_ID = p_batch_execution_id
             GROUP BY d.BATCH_EXECUTION_ID, d.BASE_DATE, d.PRODUCT_CODE, d.STAGE_CODE
      ) summary_rows;
END;
