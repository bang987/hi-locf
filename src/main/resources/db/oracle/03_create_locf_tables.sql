CREATE OR REPLACE PROCEDURE PRC_LOCF_INSERT_TARGET_CONTRACTS (
    p_batch_execution_id IN NUMBER,
    p_base_date          IN DATE
) IS
BEGIN
    INSERT INTO LOCF_TARGET_CONTRACT (
        TARGET_ID, BATCH_EXECUTION_ID, CONTRACT_ID, CONTRACT_NO, CUSTOMER_ID, PRODUCT_CODE,
        REPAYMENT_TYPE, PRINCIPAL_AMT, FEE_AMT, DIRECT_COST_AMT, ANNUAL_NOMINAL_RATE,
        EXECUTION_DATE, MATURITY_DATE, TERM_IN_MONTHS
    )
    SELECT SQ_LOCF_TARGET_CONTRACT.NEXTVAL,
           p_batch_execution_id,
           c.CONTRACT_ID,
           c.CONTRACT_NO,
           c.CUSTOMER_ID,
           c.PRODUCT_CODE,
           c.REPAYMENT_TYPE,
           c.PRINCIPAL_AMT,
           c.FEE_AMT,
           c.DIRECT_COST_AMT,
           r.ANNUAL_NOMINAL_RATE,
           c.EXECUTION_DATE,
           c.MATURITY_DATE,
           (
               (EXTRACT(YEAR FROM c.MATURITY_DATE) - EXTRACT(YEAR FROM c.EXECUTION_DATE)) * 12
               + (EXTRACT(MONTH FROM c.MATURITY_DATE) - EXTRACT(MONTH FROM c.EXECUTION_DATE))
           )
        FROM LOAN_CONTRACT c,
             LOAN_RATE r,
             LOAN_BALANCE b
       WHERE r.CONTRACT_ID = c.CONTRACT_ID
         AND b.CONTRACT_ID = c.CONTRACT_ID
         AND c.STATUS_CODE = 'ACTIVE'
         AND b.BASE_DATE = p_base_date;
END;
