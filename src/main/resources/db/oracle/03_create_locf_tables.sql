CREATE OR REPLACE PROCEDURE PRC_LOCF_CLEAR_BATCH_DATA (
    p_batch_execution_id IN NUMBER
) IS
BEGIN
    DELETE FROM LOCF_RESULT_SUMMARY
     WHERE BATCH_EXECUTION_ID = p_batch_execution_id;

    DELETE FROM LOCF_RESULT_HDR
     WHERE BATCH_EXECUTION_ID = p_batch_execution_id;

    DELETE FROM LOCF_AMORTIZATION_DTL
     WHERE BATCH_EXECUTION_ID = p_batch_execution_id;

    DELETE FROM LOCF_EIR_RESULT
     WHERE BATCH_EXECUTION_ID = p_batch_execution_id;

    DELETE FROM LOCF_CASHFLOW_BASE
     WHERE BATCH_EXECUTION_ID = p_batch_execution_id;

    DELETE FROM LOCF_TARGET_CONTRACT
     WHERE BATCH_EXECUTION_ID = p_batch_execution_id;
END;
