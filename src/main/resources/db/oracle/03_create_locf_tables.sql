CREATE OR REPLACE PROCEDURE PRC_LOCF_INSERT_RESULT_SUMMARY (
    p_batch_execution_id IN NUMBER
) IS
BEGIN
    INSERT INTO LOCF_RESULT_SUMMARY (
        SUMMARY_ID, BATCH_EXECUTION_ID, BASE_DATE, PRODUCT_CODE, CONTRACT_COUNT,
        TOTAL_INITIAL_CARRYING_AMT, TOTAL_FINAL_CARRYING_AMT, TOTAL_EFFECTIVE_INTEREST_REV, CREATED_AT
    )
    SELECT SQ_LOCF_RESULT_SUMMARY.NEXTVAL,
           summary_rows.BATCH_EXECUTION_ID,
           summary_rows.BASE_DATE,
           summary_rows.PRODUCT_CODE,
           summary_rows.CONTRACT_COUNT,
           summary_rows.TOTAL_INITIAL_CARRYING_AMT,
           summary_rows.TOTAL_FINAL_CARRYING_AMT,
           summary_rows.TOTAL_EFFECTIVE_INTEREST_REV,
           CURRENT_TIMESTAMP
      FROM (
            SELECT h.BATCH_EXECUTION_ID,
                   h.BASE_DATE,
                   h.PRODUCT_CODE,
                   COUNT(*) AS CONTRACT_COUNT,
                   SUM(h.INITIAL_CARRYING_AMOUNT) AS TOTAL_INITIAL_CARRYING_AMT,
                   SUM(h.FINAL_CARRYING_AMOUNT) AS TOTAL_FINAL_CARRYING_AMT,
                   SUM(h.TOTAL_EFFECTIVE_INTEREST_REV) AS TOTAL_EFFECTIVE_INTEREST_REV
              FROM LOCF_RESULT_HDR h
             WHERE h.BATCH_EXECUTION_ID = p_batch_execution_id
             GROUP BY h.BATCH_EXECUTION_ID, h.BASE_DATE, h.PRODUCT_CODE
      ) summary_rows;
END;
